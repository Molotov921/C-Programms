/*wap to perform relative operation*/
#include<stdio.h>
void main()
{
	int a,b;

	printf("\nEnter first value: ");
	scanf("%d",&a);
	printf("\nEnter second value: ");
	scanf("%d",&b);

	printf("\n%d",a>b);
	printf("\n%d",a>=b);
	printf("\n%d",a<b);
	printf("\n%d",a<=b);
	printf("\n%d",a==b);
}