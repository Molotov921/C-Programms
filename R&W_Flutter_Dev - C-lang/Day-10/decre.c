/*wap to perform decrement operators*/
#include<stdio.h>
void main()
{
	int a=10;
	printf("Enter the value of a = %d\n",a);
	a--;
	printf("Enter the value of a = %d\n",a);
	a--;
	printf("Enter the value of a = %d\n",a);
	a=a-1;
	printf("Enter the value of a = %d\n",a);
	a-=5;
	printf("Enter the value of a = %d\n",a);
}